// Write a Java program to display the default value of all primitive data types of Java

package javasamples;

public class Datatype {

	static int a;
	static float b;
	static boolean c;
	static double d;
	static long e;
		
	public static void main(String[] args) {

		System.out.println("Default value of int type: " +a);
		System.out.println("Default value of float type: " +b);
		System.out.println("Default value of boolean type: " +c);
		System.out.println("Default value of double type: " +d);
		System.out.println("Default value of long type: " +e);
}

}
